from attendance.repository.attendance_repository import AttendanceRepository


class AttendanceRepositoryImpl(AttendanceRepository):
    __instance = None

    def __new__(cls):
        if cls.__instance is None:
            cls.__instance = super().__new__(cls)

        return cls.__instance

    @classmethod
    def getInstance(cls):
        if cls.__instance is None:
            cls.__instance = cls()

        return cls.__instance

    def mark(self):
        pass
    